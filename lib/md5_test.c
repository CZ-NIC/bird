static void
get_md5(const char *str, char (*out_hash)[MD5_HEX_SIZE])
{
  struct md5_context ctxt;

  md5_init(&ctxt);
  md5_update(&ctxt, str, strlen(str));
  byte *hash = md5_final(&ctxt);

  int i;
  for(i = 0; i < MD5_SIZE; i++)
    sprintf(*out_hash + i*2, "%02x", hash[i]);
}

