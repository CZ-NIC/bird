static void
get_sha1(const char *str, char (*out_hash)[SHA1_HEX_SIZE])
{
  struct sha1_context ctx;
  sha1_init(&ctx);
  sha1_update(&ctx, str, strlen(str));
  byte *hash = sha1_final(&ctx);

  int i;
  for(i = 0; i < SHA1_SIZE; i++)
    sprintf(*out_hash + i*2, "%02x", hash[i]);
}

